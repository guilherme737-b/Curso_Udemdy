public class MeuPrograma {
    public static void main(String[] args) {
        calc(10, 5); // argumentos
    }

    static void calc(int x, int y){ // paremetros X , Y


        //calc > nome da função, geralmente usamos verbos para definir o que a função
        // () > usamos para definir o que chegara naquela função (nome de parametros)
        // (tipo nomeDaVariavel)
        // {} > bloco de codigo que sera executado


        System.out.println(x + y);
        System.out.println(x - y);
        System.out.println(x / y);
        System.out.println(x * y);

    }


}